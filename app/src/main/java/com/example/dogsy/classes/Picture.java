package com.example.dogsy.classes;

public class Picture {
    private int id;
    private String path;

    public Picture() {
    }

    public Picture(int id, String path) {
        this.id = id;
        this.path = path;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }
}
